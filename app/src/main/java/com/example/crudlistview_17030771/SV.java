package com.example.crudlistview_17030771;

public class SV {
    private int id;
    private String name;

    public SV(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public SV() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
